#define PS5520_MAX_AGAIN_REG    (80)

#define PS5520_GAIN_SCALE       (16)
#define PS5520_GAIN_SCALE_F     (16.0f)

#define PS5520_GAIN(idx)        ((16 + ((idx & 0x0F))) << (idx >> 4))

#define PS5520_WIDTH_ALIGN      (8)
#define PS5520_FPS_MAX          (FPS_MAX)


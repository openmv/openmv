static int reset(omv_csi_t *csi) {
    int ret = 0;
    // Write default registers
    for (int i = 0; default_regs[i][0] && ret == 0; i++) {
        if (default_regs[i][0] == 0xFF) {
            mp_hal_delay_ms(default_regs[i][1]);
        } else {
            ret |= omv_i2c_writeb(&csi->i2c_bus, csi->slv_addr, default_regs[i][0], default_regs[i][1]);
        }
    }
    return ret;
}

static int sleep(omv_csi_t *csi, int enable) {
    int ret = 0;
    const uint8_t(*regs)[2] = enable ? stream_off_regs : stream_on_regs;

    // Write default registers
    for (int i = 0; regs[i][0] && ret == 0; i++) {
        if (regs[i][0] == 0xFF) {
            mp_hal_delay_ms(regs[i][1]);
        } else {
            ret |= omv_i2c_writeb(&csi->i2c_bus, csi->slv_addr, regs[i][0], regs[i][1]);
        }
    }
    return ret;
}

static int read_reg(omv_csi_t *csi, uint16_t reg) {
    uint8_t reg_data;
    if (omv_i2c_readb(&csi->i2c_bus, csi->slv_addr, reg, &reg_data) != 0) {
        return -1;
    }
    return reg_data;
}

static int write_reg(omv_csi_t *csi, uint16_t reg, uint16_t reg_data) {
    return omv_i2c_writeb(&csi->i2c_bus, csi->slv_addr, reg, reg_data);
}

static __attribute__((unused)) int read_reg_seq(omv_csi_t *csi, uint16_t addr, size_t size, uint8_t *buf) {
    int ret = 0;
    for (size_t i = 0; i < size; i++) {
        ret |= omv_i2c_readb(&csi->i2c_bus, csi->slv_addr, addr + i, &buf[i]);
    }
    return ret;
}


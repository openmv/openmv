static int set_framesize(omv_csi_t *csi, omv_csi_framesize_t framesize) {
    int ret = 0;
    const uint8_t(*regs)[2];

    switch (framesize) {
        case OMV_CSI_FRAMESIZE_WQXGA2:
            regs = default_regs;
            break;
        case OMV_CSI_FRAMESIZE_SXGAM:
            regs = default_1296x972_regs;
            break;
        case OMV_CSI_FRAMESIZE_VGA:
            regs = default_648x486_regs;
            break;
        case OMV_CSI_FRAMESIZE_QHD: // 2560x1440
            regs = default_2560x1440_regs;
            break;
        case OMV_CSI_FRAMESIZE_FHD: // 1920x1080
            regs = default_1920x1080_regs;
            break;
        case OMV_CSI_FRAMESIZE_HD:  // 1280x720
            regs = default_1280x720_regs;
            break;
        default:
            return -1;
    }

    // SW reset
    for (int i = 0; regs[i][0] && ret == 0; i++) {
        ret |= omv_i2c_writeb(&csi->i2c_bus, csi->slv_addr, sw_reset_regs[i][0], sw_reset_regs[i][1]);
    }
    // Set resolution
    for (int i = 0; regs[i][0] && ret == 0; i++) {
        ret |= omv_i2c_writeb(&csi->i2c_bus, csi->slv_addr, regs[i][0], regs[i][1]);
    }

    return ret;
}

static int set_framerate(omv_csi_t *csi, int framerate) {
    int ret = 0;
    int16_t lpf;
    uint8_t exposure_line_h;
    uint8_t exposure_line_l;

    switch (csi->framesize) {
        case OMV_CSI_FRAMESIZE_WQXGA2:
            framerate = IM_MIN(framerate, PS5520_FPS_MAX);
            break;
        default:
            return -1;
    }

    ret |= omv_i2c_writeb(&csi->i2c_bus, csi->slv_addr, REG_BANK, 0x01);

    ret |= omv_i2c_readb(&csi->i2c_bus, csi->slv_addr, CMD_LPF_H, &exposure_line_h);
    ret |= omv_i2c_readb(&csi->i2c_bus, csi->slv_addr, CMD_LPF_L, &exposure_line_l);
    lpf = (exposure_line_h << 8) + exposure_line_l;

    ret |= omv_i2c_readb(&csi->i2c_bus, csi->slv_addr, CMD_OFFNY1_H, &exposure_line_h);
    ret |= omv_i2c_readb(&csi->i2c_bus, csi->slv_addr, CMD_OFFNY1_L, &exposure_line_l);

    int16_t exposure_line = lpf - ((exposure_line_h << 8) + exposure_line_l);

    lpf = (PIX_CLK / HTS_5M_30) / framerate;
    lpf = IM_CLAMP(lpf, VTS_5M_30, 0x10000);

    exposure_line = IM_CLAMP(exposure_line, PS5520_MIN_INT, (lpf - 3));

    ret |= omv_i2c_writeb(&csi->i2c_bus, csi->slv_addr, CMD_LPF_H, (lpf - 1) >> 8);
    ret |= omv_i2c_writeb(&csi->i2c_bus, csi->slv_addr, CMD_LPF_L, (lpf - 1) & 0xFF);

    ret |= omv_i2c_writeb(&csi->i2c_bus, csi->slv_addr, CMD_OFFNY1_H, (lpf - 1 - exposure_line) >> 8);
    ret |= omv_i2c_writeb(&csi->i2c_bus, csi->slv_addr, CMD_OFFNY1_L, (lpf - 1 - exposure_line) & 0xFF);

    ret |= omv_i2c_writeb(&csi->i2c_bus, csi->slv_addr, SENSOR_UPDATE, 0x01);
    return ret;
}


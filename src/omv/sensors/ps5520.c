static int set_auto_gain(omv_csi_t *csi, int enable, float gain_db, float gain_db_ceiling) {
    int gain = 0;
    int idx = 0;
    int ret = omv_i2c_writeb(&csi->i2c_bus, csi->slv_addr, REG_BANK, 0x01);

    if (enable == 0) {
        if ((!isnanf(gain_db)) && (!isinff(gain_db))) {
            gain = fast_roundf(expf((gain_db / 20.0f) * M_LN10) * PS5520_GAIN_SCALE_F);
        } else if ((!isnanf(gain_db_ceiling)) && (!isinff(gain_db_ceiling))) {
            gain = fast_roundf(expf((gain_db_ceiling / 20.0f) * M_LN10) * PS5520_GAIN_SCALE_F);
        }

        for (idx = 0; gain >> (4 + idx); idx++) {

        }

        idx = idx - 1;
        gain = ((idx - 1) << 4) + ((gain >> (idx - 1)) & 0x0F);
        gain = IM_CLAMP(gain, PS5520_MIN_AGAIN_REG, PS5520_MAX_AGAIN_REG);

        ret |= omv_i2c_writeb(&csi->i2c_bus, csi->slv_addr, CMD_GAIN_IDX, gain);
        ret |= omv_i2c_writeb(&csi->i2c_bus, csi->slv_addr, SENSOR_UPDATE, 0x01);
    } else {
        ret = -1;   // No AGC function of PS5520
    }

    return ret;
}

